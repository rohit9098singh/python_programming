class mathoperations:
    @staticmethod
    def add(a,b):
        return a+b
    
    @staticmethod
    def mult(a,b):
        return a*b
    
    @staticmethod
    def sub(a,b):
        return a-b
    
    @staticmethod
    def divide(a,b):
        return a/b
print("addition =",mathoperations.add(3,4))
print("addition =",mathoperations.sub(8,4))
print("addition =",mathoperations.mult(30,4))
print("addition =",mathoperations.divide(30,10))
